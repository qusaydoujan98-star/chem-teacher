// خطة التدريس: 3 حصص/أسبوع × 16 أسبوع = 48 حصة (4 أشهر)
const TEACHING_PLAN = generateTeachingPlan();

function generateTeachingPlan() {
  const allLessons = [];
  CURRICULUM.units.forEach(unit => {
    unit.lessons.forEach(lesson => {
      allLessons.push({
        ...lesson,
        unitTitle: unit.title,
        unitColor: unit.color,
        unitIcon: unit.icon
      });
    });
  });

  // ترتيب حسب الجزء ثم ترتيب الكتاب
  const weeks = [];
  let sessionCount = 0;
  let weekNum = 1;
  let weekSessions = [];

  allLessons.forEach(lesson => {
    const sessions = lesson.teachHours;
    for (let s = 0; s < sessions; s++) {
      sessionCount++;
      weekSessions.push({
        session: sessionCount,
        lessonId: lesson.id,
        lessonTitle: lesson.title,
        unitTitle: lesson.unitTitle,
        unitColor: lesson.unitColor,
        focus: s === 0 ? 'شرح المفاهيم الأساسية' :
               s === 1 ? 'حل مسائل وأمثلة امتحانية' : 'تدريب وتقييم'
      });
      if (weekSessions.length === 3) {
        weeks.push({ week: weekNum, sessions: [...weekSessions] });
        weekNum++;
        weekSessions = [];
      }
    }
  });

  if (weekSessions.length > 0) {
    weeks.push({ week: weekNum, sessions: weekSessions });
  }

  return weeks;
}

